//Um trem percorreu 453 Km em 6h, Qual a Velocidade média percorrida?

let distancia = 453;
let tempo = 6;
let velocidadeMedia = ((distancia / tempo).toFixed(1));
console.log(`A velocidade média foi de ${velocidadeMedia} km/h`);

